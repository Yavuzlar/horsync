type LanguageContextValue = {
  language: Language;
  locale: string;
  setLanguage: (language: Language) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  formatDateTime: (value: string) => string;
  formatTime: (value: string) => string;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const stored = localStorage.getItem('yavuzlar_language');
    return stored === 'en' ? 'en' : 'tr';
  });

  useEffect(() => {
    localStorage.setItem('yavuzlar_language', language);
  }, [language]);

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    locale: localeMap[language],
    setLanguage: setLanguageState,
    t: (key, params) => {
      let text = dictionaries[language][key] ?? key;
      if (params) {
        for (const [paramKey, paramValue] of Object.entries(params)) {
          text = text.replaceAll(`{${paramKey}}`, String(paramValue));
        }
      }
      return text;
    },
    formatDateTime: (value) => new Date(value).toLocaleString(localeMap[language]),
    formatTime: (value) => new Date(value).toLocaleTimeString(localeMap[language]),
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return context;
}
